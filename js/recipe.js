// 검색 기능을 위한 함수
function searchRecipesByCriteria(query, mode, allRecipes) {
    const normalizedQuery = query.toLowerCase().trim();
    const results = [];

    if (!normalizedQuery) {
        return []; // 검색어가 없으면 빈 배열 반환
    }

    for (const key in allRecipes) {
        if (allRecipes.hasOwnProperty(key)) {
            const recipe = allRecipes[key];
            let match = false;

            switch (mode) {
                case 'name':
                    if (recipe.title.toLowerCase().includes(normalizedQuery)) {
                        match = true;
                    }
                    break;
                case 'ingredient':
                    if (recipe.ingredients && Array.isArray(recipe.ingredients)) {
                        for (const ingredient of recipe.ingredients) {
                            // "간장 4큰술"에서 "간장"만으로도 검색 가능하도록 toLowerCase().includes() 사용
                            if (ingredient.toLowerCase().includes(normalizedQuery)) {
                                match = true;
                                break; // 해당 레시피에서 재료 일치 확인
                            }
                        }
                    }
                    break;
                case 'difficulty':
                    // 난이도는 정확히 일치하는 경우를 찾음 (예: "쉬움", "보통", "어려움")
                    if (recipe.difficulty && recipe.difficulty.toLowerCase() === normalizedQuery) {
                        match = true;
                    }
                    break;
                default: // 기본값은 이름으로 검색
                    if (recipe.title.toLowerCase().includes(normalizedQuery)) {
                        match = true;
                    }
            }

            if (match) {
                results.push({ key, ...recipe }); // 결과에 레시피 키와 전체 데이터 추가
            }
        }
    }
    return results;
}

// Helper function to format time (MM:SS)
function formatTime(seconds) {
    const minutes = Math.floor(seconds / 60);
    const remainingSeconds = seconds % 60;
    const paddedMinutes = String(minutes).padStart(2, '0');
    const paddedSeconds = String(remainingSeconds).padStart(2, '0');
    return `${paddedMinutes}:${paddedSeconds}`;
}

// Helper function to extract time from step string
// Returns { durationInSeconds: number, text: "X분" } or null
function extractTimeFromStep(stepText) {
    let match;

    // Priority 1: Ranges (e.g., 10-15분, 3~5초)
    // X-Y분, X~Y분, X-Y 분간, X~Y 분간
    match = stepText.match(/(\d+)\s*(?:~|-)\s*(\d+)\s*분(간)?/);
    if (match) {
        return { durationInSeconds: parseInt(match[1]) * 60, text: `${match[1]}~${match[2]}분 (타이머: ${match[1]}분)` };
    }
    // X-Y초, X~Y초, X-Y 초간, X~Y 초간
    match = stepText.match(/(\d+)\s*(?:~|-)\s*(\d+)\s*초(간)?/);
    if (match) {
        return { durationInSeconds: parseInt(match[1]), text: `${match[1]}~${match[2]}초 (타이머: ${match[1]}초)` };
    }

    // Priority 2: Single values
    // X분, X 분, X분간, X 분간
    match = stepText.match(/(\d+)\s*분(간)?/);
    if (match) {
        return { durationInSeconds: parseInt(match[1]) * 60, text: `${match[1]}분` };
    }

    // X초, X 초, X초간, X 초간
    match = stepText.match(/(\d+)\s*초(간)?/);
    if (match) {
        return { durationInSeconds: parseInt(match[1]), text: `${match[1]}초` };
    }

    // X시간, X 시간, X시간 동안
    match = stepText.match(/(\d+)\s*시간(?: 동안)?/); // ' 동안' is optional
    if (match) {
        return { durationInSeconds: parseInt(match[1]) * 3600, text: `${match[1]}시간` };
    }

    return null;
}


